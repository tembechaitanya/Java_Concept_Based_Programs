
class ArrayXX
{
    public static void main(String A[])
    {
        int Arr[] = {10,20,30,40};

        System.out.println("\nArr[0] : " + Arr[0]);
        System.out.println("Arr[1] : " + Arr[1]);
        System.out.println("Arr[2] : " + Arr[2]);
        System.out.println("Arr[3] : " + Arr[3]);

        int Brr[] = new int[4];
        Brr[0] = 10;
        Brr[1] = 20;
        Brr[2] = 30;
        Brr[3] = 40;

        System.out.println("\nBrr[0] : " + Brr[0]);
        System.out.println("Brr[1] : " + Brr[1]);
        System.out.println("Brr[2] : " + Brr[2]);
        System.out.println("Brr[3] : " + Brr[3]);

        int crr[] = new int[]{10,20,30,40};

        System.out.println("\ncrr[0] : " + crr[0]);
        System.out.println("crr[1] : " + crr[1]);
        System.out.println("crr[2] : " + crr[2]);
        System.out.println("crr[3] : " + crr[3]);


    }
}