
class Datatype1
{
    public static void main(String B[])
    {
        int A = 5;
        float myFloatNum = 10.50f;
        double D = 10.20;
        long p = 20;
        char myLetter = 'H';
        boolean nybool = true;
        short sss = 10;
        byte bbb = 0;
        String s11 = "Eka Dajiba";

        int Arr[] = new int[]{10,20,30};

        System.out.println("Value in variable A is "+ A);
        System.out.println("Value in variable myFloatNum is " + myFloatNum);
        System.out.println("Value in variable D is " + D);
        System.out.println("Value in variable p is " + p);
        System.out.println("Value in variable myLetter is " + myLetter);
        System.out.println("Value in variable sss is " + sss);
        System.out.println("Value in variable nybool is " + nybool);
        System.out.println("Value in variable bbb is " + bbb);
        System.out.println("Value in string s11 is " + s11);


        System.out.println("Value in array index 0 is " + Arr[0]);
        System.out.println("Value in array index 1 is " + Arr[1]);
        System.out.println("Value in array index 2 is " + Arr[2]);

    }
}