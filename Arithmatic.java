package Marvellous;

public class Arithmatic
{
    public int Val1;
    public int Val2;

    public Arithmatic(int A, int B)
    {
        Val1 = A;
        Val2 = B;
    }
 
    public int Addition()
    {
        return Val1 + Val2;
    }

    public int Subtraction()
    {
        return Val1 - Val2;
    }    
}